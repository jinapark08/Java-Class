package javaapp0408;

public class operator3 {

	public static void main(String[] args) {
		int n = 18;
		// <<: 한번 할 때 마다 2배씩 증가 
		//곱하기 2한 효과
		System.out.println("18<<2:"+(n<<2));
		//32번 이상 하라고 하면 32로 나눈 나머지 만큼만 수행
		System.out.println("18<<35:"+(n<<35));

		System.out.println("18>>2:"+(n>>2));//=4
		System.out.println("18>>35:"+(n>>35));//2
		
		
	}
	
	
}

